            <p>

                Excellences, Madame, Monsieur les membres d’honneur de l’ASBL FINS/WINS
                Mesdames, Messieurs les membres du Conseil d’Administration de l’ASBL FINS/WINS
                Honorables invités, <br>
                Chers amis,<br>

                C’est pour moi un immense plaisir et je ressens une émotion particulière en prenant la
                parole en cette occasion exceptionnelle. Je voudrais, au nom des membres du Conseil
                d’Administration et d’honneur de l’ASBL FINS/WINS vous souhaiter la bienvenue et vous
                dire combien nous nous sentons honorer par votre présence à cette modeste cérémonie de
                lancement officiel des activités de notre ASBL Femmes Interface Nord-Sud (FINS).<br>

                Il s’agit, certes, d’une ASBL dont l’officialisation des activités aurait pu se faire
                comme pour la plupart de celles qui se créent ici ou ailleurs. Toutefois, si nous avions
                tenu à vous convier à cette soirée c’est pour vous faire connaître la spécificité de
                notre démarche associative; partager nos rêves, ambitions et espoirs d’un autre monde;
                un monde où l’univers des impossibles peut devenir possible; un monde où les inégalités
                et exclusions peuvent faire place à l’intégration et l’équité, en un mot un monde où le
                progrès, le développement durable et véritablement humain se conçoivent dans la
                fraternité et la solidarité entre les peuples du Nord et du Sud.<br>

                Ceci étant, permettez-moi, distingués invités, de vous présenter succinctement l’ASBL
                FINS. Lors de cette présentation, je m’efforcerai de mettre l’accent sur ce qui fait la
                spécificité de notre association. Celle-ci sera articulée autour des points suivants:<br>
            </p>

            <li>Historique et processus de création;</li>
            <li>But principal – cadre thématique et domaines d’actions;</li>
            <li>Stratégie globale d’intervention;</li>
            <li>Sa structuration organisationnelle et fonctionnelle;</li>
            <li>Priorités pour 2009 et attentes</li>

