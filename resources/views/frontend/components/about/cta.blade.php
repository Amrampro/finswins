            <h3>l’ASBL FINS-WINS: Une histoire individuelle où se mêle soif de connaissance
                scientifique et désir d’Actions concrètes</h3>

            <div class="row">
                <div>
                    <p>L’origine et le processus de création de l’ASBL FINS, s’inscrivent dans un cadre
                        individuel et une démarche intellectuelle, celle d’une femme du Sud qui vit depuis plus
                        de 20 ans au Nord et qui est curieuse, d’une part, de savoir pourquoi après plus de 40
                        ans d’indépendances formelles, la plupart des pays du Sud, sont toujours dans une
                        situation de malade sous perfusion en état stationnaire; et, d’autre part, celle d’une
                        étudiante désireuse de faire des recherches doctorales à l’Institut d’études du
                        développement de l’Université Catholique de Louvain (UCL).</p>
                </div>
                <div>
                    <p>En effet, après mes études de Sciences politiques orientation Relations Internationales
                        et d’Etudes de développement, sanctionnés respectivement par les diplômes de Licence en
                        Relations internationales et de Master (troisième cycle) avec une spécialisation en «
                        Conception et formulation des projets », je me sui dit que la meilleure façon pour moi
                        de comprendre cette situation des pays du Sud et de contribuer à y apporter des
                        solutions concrètes, c’est d’approfondir la réflexion et poursuivre les recherches dans
                        ce sens.</p>
                </div>
                <div>
                    <p>C’est ainsi que, grâce aux conseils avisés et soutien de ma promotrice ici présente,
                        Professeur Isabel Yépez, et de feu Professeur Séverin Cécile Abéga mon co-promoteur,
                        j’ai élaboré un projet de thèse et sur la thématique du « Genre et Développement. Sur la
                        base de ce projet, j’ai été admise par la Commission doctorale à entamer une thèse à
                        compter de l’année académique 2007-2008.</p>
                </div>
            </div>
            <h4>L’année d’exploration et de préparation</h4>
            <p>Consciente du fait que cela va nécessiter un engagement total et des sacrifices énormes,
                notamment aux plans personnel et familial, j’avais sollicité et obtenu des membres de
                mon comité d’encadrement une année exploratoire afin de mieux me préparer avant
                l’inscription officielle qui a eu lieu au début de cette année académique. Cette phase
                de préparation devait me permettre de réaliser deux objectifs: d’une part, effectuer des
                missions de terrain au Cameroun pour voir si je pouvais obtenir les données pertinentes
                ou informations nécessaires pour traiter mon sujet; et, d’autre part, faire la revue de
                la littérature pour cerner les contours de ma thématique et problématique.</p>

            <h4>Séjour de terrain avec des « lunettes de chercheur et casquette de femme »</h4>
            <p>Après avoir effectué deux séjours au Cameroun au courant de cette année 2008, j’ai pris
                conscience que la marginalisation et la non participation effective des femmes dans les
                sphères sociale, économique et politique, et surtout la pauvreté féminine ne sont pas
                qu’une vue de l’esprit. Certes, il y a des femmes ministres, des femmes députés, des
                femmes chefs d’entreprises, des femmes à des postes de direction dans l’Administration
                Publique. Mais combien sont-elles et quelle est leur réelle influence ? Ce n’est pas la
                situation encore moins la condition de ces femmes qui m’a donné l’idée de créer une
                ASBL. <br>

                En visitant quelques écoles et centres de santé ici et là au Cameroun ; en écoutant les
                témoignages des personnes rencontrées; en lisant les journaux et en regardant la
                télévision, je me suis rendu compte du gouffre qu’il y a entre le Nord et le Sud,
                notamment en termes d’accessibilité aux services essentiels de santé (soins primaires,
                santé maternelle et infantile, etc.) et de l’éducation formelle, non formelle et à la
                formation professionnelle.<br>

                Pour mieux appréhender la situation, j’ai consulté les statistiques nationales du pays.
                Les données objectives sont éloquentes. Elles confirment mon postulat selon lequel les
                catégories des populations les plus touchées sont les femmes et les enfants (surtout
                dans les villages ou régions enclavées). D’où le questionnement : dois-je me limiter à
                mon rôle de chercheuse en quête de données pour sa recherche académique ou faire quelque
                chose pour contribuer à apporter un tant soit peu des réponses concrètes à cette
                situation de souffrance humaine et de précarisation et ?<br>

                En partageant cette préoccupation avec mes proches, mon époux ici présent, et quelques
                amis dans les milieux universitaires, j’ai eu l’assurance que mener une recherche
                académique et agir ne sont pas incompatibles. D’ailleurs, m’ont-ils rassurés, une thèse
                devra nécessairement avoir un double enjeu: -- Scientifique (qui consiste à contribuer à
                l’amélioration du savoir dans un champ disciplinaire donné), et pratique (il s’agit de
                proposer des solutions et d’apporter des réponses concrètes aux problèmes de société).
                D’où le déclic ayant conduit à la fondation de l’ASBL FINS/WINS.</p>

