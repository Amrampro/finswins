                <p>La situation et la condition de la femme dans la société constituent un sujet constant de
                    préoccupations des politiques, des décideurs, des promoteurs de développement, des acteurs de la
                    société civile, tant du Nord que du Sud. <br>

                    Car, le dénominateur commun de ces femmes, voire la discrimination de celles-ci dans les champs
                    politique, économique, social et culturel est la prédominance de l’ordre masculin qui détermine
                    leur statut, leur rôle et place dans les sociétés. Cela a pour corollaire la sous-estimation,
                    voire la marginalisation de celles-ci dans les champs social, économique et politique.<br>

                    Si dans les pays du Nord des progrès tangibles sont observables en termes d’amélioration de la
                    condition de la femme dans ces domaines, c’est moins le cas des femmes du Sud. Au Sud, la
                    condition féminine n’est guère reluisante. Elle est déterminée, d’une part par un système de
                    valeurs, des contraintes socioculturelles et de multiples résistances; et, d’autre part, par le
                    contexte du sous-développement.<br>

                    Pourtant, qu’elles soient du Nord ou du Sud, les femmes sont toutes confrontées aux contraintes,
                    enjeux et défis de la mondialisation.

                    Ce phénomène, en même temps qu’il génère des externalités négatives (détérioration des termes de
                    l’échange, exclusion, inégalités, pauvreté, conflits, etc.) offre des opportunités
                    d’amélioration du statut de la femme et de son intégration au processus global de développement
                    humain harmonieux. Pour faire face aux problèmes et défis communs résultant d’un tel contexte,
                    de nombreuses associations féminines se créent au Nord comme au Sud.<br>

                    En dépit de la pluralité de ces associations, de la diversité de leur champ ou domaines
                    d’intervention ainsi que des moyens mobilisés, l’impact des actions en faveur de la condition
                    féminine reste faible.<br>

                    C'est dans ce contexte que l'ASBL FINS-WINS a été fondée. Elle a pour but essentiel de
                    positionner la femme comme citoyenne du monde, sujet de son histoire et actante des mutations
                    sociaux-économiques et politico-culturelles.<br>

                    L’équipe de l’ASBL FINS-WINS, composée de femmes et d’hommes de nationalités, de cultures
                    différentes et de profils transdisciplinaires, encadre les réflexions et pilote des actions de
                    terrains.<br>

                    La stratégie globale de l’ASBL FINS-WINS est la Recherche-Action. Celle-ci se fonde sur une
                    démarche scientifique combinant la théorie et la pratique. Sa finalité consiste, d’une part, à
                    mettre en exergue le « plafond de verre »; et, d’autre part, à identifier les logiques d’actions
                    et les stratégies lui permettant d’être actrice incontournable du développement.<br><br>

